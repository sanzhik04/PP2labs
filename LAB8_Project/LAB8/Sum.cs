using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LAB8
{
    class Sum
    {
        int a;
        int b;

        public Sum(int a, int b)
        {
            this.a = a;
            this.b = b;
        }

        public int Find()
        {
          
            return a + b;
        }
    }
}
